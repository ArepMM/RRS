bool TcpServer::start()
{
    if (server == Q_NULLPTR)
        return false;

    connect(server, &QTcpServer::newConnection,
            this, &TcpServer::onClientConnection);

    if (!server->listen(QHostAddress::Any, tcp_config.port))
        return false;

    return true;
}

