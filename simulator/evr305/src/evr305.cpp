void EVR305::ode_system(const state_vector_t &Y,
                                     state_vector_t &dYdt,
                                     double t)
{
    double s1 = abs(a) * pf(a) * ((p_ar - Y[0]) * K[0]);

    double s2 = (p_ar - pzpk_in) * K[3] * cut(pf((Y[0] - ptc) * A1) * k[0], 0.0, 1.0);

    Qar_out = Qar_in - s1 * K[2] - s2 * K[4];

    dYdt[0] = (s1 - (!abs(a) * Y[0] * K[1])) / Vpk;
}

