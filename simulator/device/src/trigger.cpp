#include    "trigger.h"

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
Trigger::Trigger(QObject *parent) : QObject(parent)
    , state(false)
    , sound_on(sound_state_t())
    , sound_off(sound_state_t())
{

}

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
Trigger::~Trigger()
{

}

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
void Trigger::set()
{
    if (state)
        return;

    state = true;
    sound_on.play = true;
    sound_off.play = false;
}

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
void Trigger::reset()
{
    if (!state)
        return;

    state = false;
    sound_off.play = true;
    sound_on.play = false;
}

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
bool Trigger::getState() const
{
    return state;
}

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
sound_state_t Trigger::getSoundOn() const
{
    return sound_on;
}

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
sound_state_t Trigger::getSoundOff() const
{
    return sound_off;
}
